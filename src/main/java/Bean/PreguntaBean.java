package Bean;

public class PreguntaBean {

    private String CodPre;
    private String DesPre;
    private String OrdPre;

    public String getCodPre() {
        return CodPre;
    }

    public void setCodPre(String codPre) {
        CodPre = codPre;
    }

    public String getDesPre() {
        return DesPre;
    }

    public void setDesPre(String desPre) {
        DesPre = desPre;
    }

    public String getOrdPre() {
        return OrdPre;
    }

    public void setOrdPre(String ordPre) {
        OrdPre = ordPre;
    }
}
