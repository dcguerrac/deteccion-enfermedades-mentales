package Bean;

public class ResultadoBean {

    private String CodRes;
    private String EstRes;
    private String DiaRes;
    private int PunRes;
    private String CodPer;

    public String getCodRes() {
        return CodRes;
    }

    public void setCodRes(String codRes) {
        CodRes = codRes;
    }

    public String getEstRes() {
        return EstRes;
    }

    public void setEstRes(String estRes) {
        EstRes = estRes;
    }

    public String getDiaRes() {
        return DiaRes;
    }

    public void setDiaRes(String diaRes) {
        DiaRes = diaRes;
    }

    public int getPunRes() {
        return PunRes;
    }

    public void setPunRes(int punRes) {
        PunRes = punRes;
    }

    public String getCodPer() {
        return CodPer;
    }

    public void setCodPer(String codPer) {
        CodPer = codPer;
    }
}
