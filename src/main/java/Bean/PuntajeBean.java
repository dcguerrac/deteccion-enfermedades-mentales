package Bean;

public class PuntajeBean {
    private String CodPun;
    private String DesPun;
    private int NumPun;

    public String getCodPun() {
        return CodPun;
    }

    public void setCodPun(String codPun) {
        CodPun = codPun;
    }

    public String getDesPun() {
        return DesPun;
    }

    public void setDesPun(String desPun) {
        DesPun = desPun;
    }

    public int getNumPun() {
        return NumPun;
    }

    public void setNumPun(int numPun) {
        NumPun = numPun;
    }
}
